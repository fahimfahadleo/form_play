import 'package:flutter/widgets.dart';

class Functions{
  BuildContext buildContext;
  Functions(this.buildContext);
  double getWidth() {
    double width = MediaQuery.of(buildContext).size.width;
    print('Screen width: $width.');
    return width;
  }

  getHeight() {
    double height = MediaQuery.of(buildContext).size.height;
    print('Screen height: $height.');
    return height;
  }

  // Color color3 = HexColor("#88b74093");
  String? validateEmail(String? value) {
    const pattern = r"(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'"
        r'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-'
        r'\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*'
        r'[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4]'
        r'[0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9]'
        r'[0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\'
        r'x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])';
    final regex = RegExp(pattern);

    return value!.isNotEmpty && !regex.hasMatch(value)
        ? 'Enter a valid email address'
        : null;
  }
  String? validatePassword(String? value) {
    return value!.isNotEmpty && value.length<5
        ? 'Password must be of 6 or more characters'
        : null;
  }



}
